// ─── Merge into translations.ts ───────────────────────────────────────────────
const translationsFile = fs.readFileSync(
  path.join(__dirname, '../src/lib/translations.ts'), 'utf8'
);

let updatedFile = translationsFile;
let totalAdded = 0;

for (const [lang, entries] of Object.entries(NEW_TRANSLATIONS)) {
  // Find the language block
  const langRegex = new RegExp(`(${lang}:\\s*\\{)([^}]*)`, 's');
  const match = updatedFile.match(langRegex);
  if (!match) {
    console.log(`⚠ Language block not found: ${lang}`);
    continue;
  }
  
  // Get existing keys in this block
  const existingBlock = match[2];
  const existingKeyMatches = existingBlock.match(/"([^"]+)":/g) || [];
  const existingKeys = new Set(existingKeyMatches.map(k => k.replace(/[":]/g, '')));
  
  // Build new entries to add
  const newEntries = [];
  let added = 0;
  for (const [key, value] of Object.entries(entries)) {
    if (!existingKeys.has(key)) {
      // Escape quotes in key and value
      const escapedKey = key.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
      const escapedValue = value.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
      newEntries.push(`    "${escapedKey}": "${escapedValue}",`);
      added++;
    }
  }
  
  if (newEntries.length > 0) {
    // Insert new entries at the end of the language block
    const insertionPoint = `${match[1]}${match[2]}`;
    const replacement = `${match[1]}${match[2]}\n${newEntries.join('\n')}`;
    updatedFile = updatedFile.replace(insertionPoint, replacement);
    console.log(`✓ ${lang}: +${added} new translations`);
    totalAdded += added;
  } else {
    console.log(`  ${lang}: no new entries needed`);
  }
}

fs.writeFileSync(path.join(__dirname, '../src/lib/translations.ts'), updatedFile);
console.log(`\n✅ Done! Added ${totalAdded} translations total to src/lib/translations.ts`);
